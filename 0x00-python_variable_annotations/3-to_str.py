#!/usr/bin/env python3
"""
3-to_str.py

This module defines a type-annotated function `to_str` that returns the string
representation of a float.
"""


def to_str(n: float) -> str:
    """
    Convert a float to its string representation.

    Args:
        n (float): The float value to convert.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
